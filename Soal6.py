# MULAI
# INPUT NILAI JARI-JARI / TENTUKAN NILAI JARI JARI DARI DIAMETER
# INPUT NILAI TINGGI
# LAKUKAN PROSES PERHITUNGAN VOLUME KERUCUT: V = 1/3 * phi * (r^2) * t
# BERIKAN OUTPUT HASIL VOLUME KERUCUT
# SELESAI

diameter = 5
r = diameter / 2
t = 4

